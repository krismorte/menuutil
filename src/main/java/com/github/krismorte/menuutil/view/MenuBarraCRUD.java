    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!txtMenuBarra.getText().equals("")) {
            if (lista.isEmpty()) {
                criaMenuBarra(txtMenuBarra.getText(), true);
                inicializaTela();
            } else {
                if (!InspectorUtil.existingName(lista, txtMenuBarra.getText())) {
                    criaMenuBarra(txtMenuBarra.getText(), false);
                    inicializaTela();
                } else {
                    JOptionPane.showMessageDialog(null, "Nome j√° existe!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite um nome!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void criaMenuBarra(String texto, boolean principal) {
        MenuBarra menuBarra = new MenuBarra();
        menuBarra.setNome(texto);
        menuBarra.setPrincipal(principal);

        menuBarraDao.save(menuBarra);
    }

    private void inicializaTela() {
        txtMenuBarra.setText("");
        preencheTabela();
    }

