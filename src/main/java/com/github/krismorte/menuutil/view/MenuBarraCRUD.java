    public void preencheLinhas(List<MenuBarra> list) {
        linhas = new Object[list.size()][colunas.length];
        if (list.size() > 0) {
            for (int i = 0; i < list.size(); i++) {
                for (int z = 0; z < colunas.length; z++) {
                    Object[][] obj = {{list.get(i).getId(), list.get(i), list.get(i).getPrincipal()}};
                    linhas[i][z] = obj[0][z];//Rows are dynamics
                }
            }
        }
    }

    private void preencheTabela() {
        lista = menuBarraDao.listAll();
        preencheLinhas(lista);

        panelBarras.removeAll();

        //panelBarras.add(InspectorUtil.jTableDecorator(panelBarras, colunas, linhas));
        panelBarras.add(InspectorUtil.jTableDecoratorWithMenuPopup(panelBarras, PopupMenu.getSimpleVersion(this, null, 1, emf), colunas, linhas));
        panelBarras.validate();
    }

    @Override
    public void atualizarTela() {
        preencheTabela();
    }

